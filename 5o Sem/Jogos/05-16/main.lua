function love.load()
    LG.setBackgroundColor(1,1,1)
    --LG.setColor(0,0,1,0.8)
    larg = LG.getWidth() / 15
    alt = LG.getHeight() / 10
    -- Mapeamento com sprite
    imagem = LG.newImage('mundo.png')
    imgLarg = imagem:getWidth()
    imgAlt = imagem:getHeight()
    tam = 32

    -- Coletar os quadril√°teros
    quadros = {}
    quadros[1] = LQ(2 * tam, 0 * tam, tam, tam, imgLarg, imgAlt)
    quadros[2] = LQ(4 * tam, 0 * tam, tam, tam, imgLarg, imgAlt)
    quadros[3] = LQ(0 * tam, 20 * tam, tam, tam, tam, imgLarg, imgAlt)
    quadros[4] = LQ(3 * tam, 9 * tam, tam, tam, tam, imgLarg, imgAlt)
end

